<h2>关于球员简体中文译名的解释</h2>
<h4>◉总体原则：尊重事实，尊重文化，兼顾习惯</h4>
<h4>◉翻译用字</h4>
<div>球员名字母语为汉语(简/繁)、日语、韩语/朝鲜语、越南语时，应该按其名字实际对应的汉语字或同音字里较为常见的译法进行翻译，并简体化。如Naomi Osaka(大坂なおみ)译为“大坂直美”，非“大阪直美”或“大坂娜奥米”；若其姓名的一部分本来就是按外文所取，则该部分按音译。如Taro Daniel(ダニエル太郎)，其姓ダニエル是根据英文Daniel翻译而来，故其姓采用音译，而名直接使用汉字，译成丹尼尔太郎。其他母语名字应该按音译形式转换成简体中文。音译的准则是：</div>
<div>&nbsp;&nbsp;&nbsp;a) 根据其名字在其母语中的发音进行音译。如 Felix Auger-Aliassime，其中Auger为其母姓，其母为加拿大魁北克省人，应遵循法语读音，翻译成“奥热”，而非英语读音“奥格尔”。Aliassime为父姓，其父来自非洲的多哥，官方语言亦为法语，翻译成“阿利亚西姆”</div>
<div>&nbsp;&nbsp;&nbsp;b) 无法知晓其母语的，按其名字在所属国家/地区的官方语言中的发音进行音译</div>
<div>&nbsp;&nbsp;&nbsp;c) 虽然媒体的译法错误，但成名已久且多方称谓较为统一的，或者与正确译名差别不大的，按约定俗成的译法翻译。如Djokovic，正确音译为“乔科维奇”，但仍译为约定俗成的“德约科维奇”；Coric，普遍译法为“丘里奇”，正确音译为“乔里奇”，差别不大，不作更改；Herbert，普遍译法“赫伯特”，与正确译法“埃贝尔”相去甚远，更改为正确译法</div>
<div>&nbsp;&nbsp;&nbsp;d) 名字的罗马字形式含有tr，dr字样的，无论其在母语中是否连读，均按te,l与de,l组成双音节词。如Tracy->特蕾西，Petra->佩特拉</div>
<div>&nbsp;&nbsp;&nbsp;e) 华裔球员如果有中文名，则使用其中文名，而非其母语音译。如 Claire Liu 译为“刘婧文”
<h4>◉姓名序与连字符</h4>
<div>1、来自中国、中国香港、中华台北、日本、韩国、越南、<b>匈牙利</b>的球员，采用东方姓名序（姓在前，名在后）；否则采用西方姓名序（名在前，姓在后）</div>
<div>2、来自中国、中国香港、中华台北、日本、韩国、越南的球员，姓与名连写，不加任何连字符；除此之外，姓与名之间用·分隔</div>
<div>3、如果球员的姓包含多部分，或球员的名包含多部分，则各部分间用-连接。如 Maria Jose Martinez Sanchez 译为“玛丽亚-何塞·马丁内斯-桑切斯”</div>
<h4>◉人名简称</h4>
<div>1、来自中国、中国香港、中华台北、日本、韩国、越南的球员简称与全名相同；否则，在只用姓不致混淆的情况下，用姓作为球员简称</div>
<div>2、若多名球员的姓相同，为了方便区分，在姓之前加上名的中文形式的首字，并用-连接。如“阿-拉德万斯卡”。约定俗成的除外，如“小威廉姆斯”</div>
<div>3、若球员的姓翻译成中文只有一个字，则在姓之前加上名的全称，并用-连接。如“达里安-金”</div>
<div>4、若球员的姓含有多个部分：</div>
<div>&nbsp;&nbsp;&nbsp;a) 若姓包含了父姓与母姓，用父姓作为简称。如 Felix Auger-Aliassime 按父姓Aliassime，简称“阿利亚西姆”，Beatriz Haddad Maia 按父姓Maia，简称“玛雅”，Sara Sorribes Tormo 按父姓Sorribes，简称“索里维斯”</div>
<div>&nbsp;&nbsp;&nbsp;b) 若姓包含了自己本姓与配偶姓氏，用自己本姓作为简称。如 Bethanie Mattek-Sands 简称“马泰克”</div>
<div>&nbsp;&nbsp;&nbsp;c) 未按上述原则但已约定俗成的，按约定俗成的译法。如 Roberto Bautista Agut 应译为“包蒂斯塔”，但仍沿用约定俗成的“阿古特”</div>
<div>&nbsp;&nbsp;&nbsp;d) 若以上简称会致混淆的，则用整个姓氏部分作为简称，用-连接。如 Arantxa Sanchez Vicario 简称“桑切斯-维卡里奥”</div>
